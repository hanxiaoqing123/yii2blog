    /**
     * Login action.
     *
     * @return string
     */
    public function actionLogin()
    {
        var_dump(Yii::$app->helper->property);
        echo "<br/>";
        var_dump(Yii::$app->helper->checkedMobile('185xxx'));
        echo "<br/>";
        echo Yii::getVersion();
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $this->layout = 'blank';

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        } else {
            $model->password = '';

            return $this->render(
                'login',
                [
                    'model' => $model,
                ]
            );
        }
    }

    /**
     * Logout action.
     *
     * @return string
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }
